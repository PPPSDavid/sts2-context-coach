/// <summary>Cached deck-level context generated by periodic LLM summary calls.</summary>
public sealed class LlmDeckProfile
{
    public required string Signature { get; init; }
    public DateTimeOffset GeneratedUtc { get; init; }
    public int? GeneratedFloor { get; init; }
    public int Version { get; init; } = 1;

    public string CorePlan { get; init; } = "";
    public List<string> Enablers { get; init; } = [];
    public List<string> Payoffs { get; init; } = [];
    public List<string> Risks { get; init; } = [];
    public List<string> Interactions { get; init; } = [];

    public List<string> ToSummaryLines(int maxLines)
    {
        var lines = new List<string>();
        if (!string.IsNullOrWhiteSpace(CorePlan))
            lines.Add($"plan: {CorePlan.Trim()}");

        AddBucket(lines, "enablers", Enablers, 3);
        AddBucket(lines, "payoffs", Payoffs, 3);
        AddBucket(lines, "risks", Risks, 2);
        AddBucket(lines, "interactions", Interactions, 4);

        if (maxLines <= 0)
            return [];
        return lines.Take(maxLines).ToList();
    }

    private static void AddBucket(List<string> lines, string name, List<string> source, int limit)
    {
        if (source.Count == 0) return;
        var text = string.Join("; ", source.Where(s => !string.IsNullOrWhiteSpace(s)).Take(limit).Select(s => s.Trim()));
        if (string.IsNullOrWhiteSpace(text)) return;
        lines.Add($"{name}: {text}");
    }
}

internal readonly struct LlmDeckProfileRefreshDecision
{
    public required bool ShouldSchedule { get; init; }
    public required string Signature { get; init; }
    public required string Reason { get; init; }
}
